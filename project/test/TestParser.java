import compiler.Lexer.Lexer;
import compiler.Lexer.Symbol;
import compiler.Parser.Parser;
import compiler.Parser.Program;

import java.io.StringReader;
import java.io.IOException;

public class TestParser {
    public static void main(String[] args) {
        String source = "$Good luck\n" +
                "\n" +
                "final message string = \"Hello\";\n" +
                "final run bool = true;\n" +
                "final k int = 3;\n" +
                "\n" +
                "Point rec {\n" +
                "    x int;\n" +
                "    y int;\n" +
                "}\n" +
                "\n" +
                "a int = 3;\n" +
                "b float = 2.0;\n" +
                "\n" +
                "\n" +
                "fun square(v int) int {\n" +
                "    return v*v;\n" +
                "}\n" +
                "\n" +
                "fun main() {\n" +
                "    value int = readInt();\n" +
                "    p Point = Point(a, a+value);\n" +
                "    writeInt(square(value));\n" +
                "    writeln(\"\");\n" +
                "    i int;\n" +
                "    for (i, 1, a, 1) {\n" +
                "        while (value!=0) {\n" +
                "            if (run){\n" +
                "                value = value - 1;\n" +
                "            } else {\n" +
                "                write(message);\n" +
                "            }\n" +
                "        }\n" +
                "    }\n" +
                "    i = (i+2)*2;\n" +
                "}";

        try {
            StringReader reader = new StringReader(source);
            Lexer lexer = new Lexer(reader);
            Parser parser = new Parser(lexer);
            Program ast = parser.parseProgram();
            System.out.println("===== AST Generated by the Parser =====");
            System.out.println(ast);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
}
